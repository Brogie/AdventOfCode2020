        private static int Part1(int down, int right)
        {
            int treeCount = 0;
            int x = 0;
            int y = 0;
            int maxX = map[0].Length - 1;
            int maxY = map.Length - 1;

            while(y <= maxY)
            {
                if (map[y][x] == '#') {  treeCount++; }
                y+=down;
                x+=right;
                if (x > maxX) { x = (x % maxX) - 1; }
            }

            return treeCount;
        }

